<h2>Smoothie</h2>
<h4>Smart Smoothie (1porsjon)</h4>
	<ul>
		<li>3dl appelsinjuice</li>
		<li>½ stk brokkoli</li>
		<li>200g skogsbærblanding</li>
		<li>6 stk isbiter</li>
	</ul>
	<p>Ha appelsinjuicen I en blender. Brekk opp brokkolien og ha den sammen I blenderen med skogsbærblandingen og isbitene. 
		Kjør det hele til du har fått en jevn smoothie blanding.</p>
	<img src="img/smart-smoothie.jpg" width="500px" alt="smart-smoothie">
<br>
<h4>Gulrot- og ananas smoothie (1porsjon)</h4>
	<ul>
		<li>2 skiver frisk ananas</li>
		<li>1 dl appelsinjuice</li>
		<li>1 stk gulrot</li>
		<li>2 dl yoghurt naturell</li>
	</ul>
	<p>Rens og skjær ananas og gulrot I biter. Ha appelsinjuicen I mikseren først – deretter yoghurt, ananas og gulot. 
		Kjør blandingen til en du har fått en glatt smotthie</p>
