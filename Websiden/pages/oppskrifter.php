<h4>Brokkoliwok med spekeskinke, soltørkede tomater og pinjekjerner (2porsjoner)</h4>
	<ul>
		<li>1 Rødløk, i tynne båter</li>
		<li>2 Fedd hvitløk, finkakket</li>
		<li>1 Stor brokkoli, i små buketter (bruk gjerne stilken og, mye næring i den)</li>
		<li>4-6 Skiver spekeskinke, i strimler (kan evt. bruke svinekjøtt også)</li>
		<li>5-6 Soltørkede tomater, i biter (billig om man kjøper på glass i olje)</li>
		<li>En neve pinjekjerner, ristet </li>
		<li>Persille, hakket</li>
		<li>Nykvernet pepper</li>
	</ul>
	<p>Fres løken i litt smør/olje til den er blank og myk. Ha i hvitløken og brokkolien, og fres til brokkolien er gjennomvarm 
		og passe mør. Pass på at hvitløken ikke blir brent (tilsett evt. litt ekstra smør/olje og salt for å unngå dette). Rør i tomatene og spekeskinken.</p>
	<p>Når tomatene er  gjennomvarme tilsettes de ristede pinjekjernene og den hakkede persillen. Smak det hele til med nykvernet 
		pepper, og evt. salt. Server det hele med godt brød, evt foccacia. Om du ikke har kronene til spekeskinken, så kan dette også 
		være en fin vegetarrett, ved å evt. bare legge til noen ekstra grønnsaker, eller bare ha den som den er!</p>
	<img src="img/brokkoliwok.jpg" width="500px" alt="Brokkoliwok">
