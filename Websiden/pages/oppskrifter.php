 <h4>Matpakkeforslag - Mango og skinkewrap</h4>
 	<ul>
 		<li>Lefse</li>
 		<li>Kjøtt/Skinke av eget valg</li>
 		<li>Roccola salat</li>
 		<li>Vårløk</li>
		<li>Mango</li>
		<li>Cherrytomater</li>
		<li>Kesam naturel</li>
	</ul>
	<p>Man legger tar først å smører litt naturell kesam over lefsen, deretter legger man på den skinken man ønsker, så strør 
		man over den salaten man har valgt, vårløken, cherrytomatene, og mangoskivene. Så bretter/ruller man det sammen som en 
		taco og pakker det inn i plastfolie, så den holder seg fint sammen brettet/rullet til lunchen på skolen.</p>
