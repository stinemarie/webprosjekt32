<h4>Laks med sesamfrø, soyasaus og lun pak choysalat (3porsjoner)</h4>
	<p>Laks med samfrø:</p>
	<ul>
		<li>500-600g laksefilet, i porsjonsstykker (kjøp frossent og tin, billigst)</li>
		<li>salt og pepper</li>
		<li>olje/smør til steking</li>
		<li>sesamfrø</li>
	</ul>
	<p>Lun pak choysalat:</p>
	<ul>
		<li>1 pak choy (kinasalat), grovt strimlet</li>
		<li>1 rødløk, i tynne båter</li>
		<li>1 rød paprika, i strimler</li>
		<li>2 gulrøtter, i strimler</li>
	</ul>
	<p>Spicy soyasaus:</p>
	<ul>
		<li>1 fedd hvitløk, finhakket</li>
		<li>ca ¼ rød chili (eller mer etter ønske), finhakket</li>	
		<li>ca ½ lime, saft og skall</li>
		<li>2-3 ss olivenolje</li>
		<li>2-3 ss soyasaus</li>
		<li>1-2 ss ketjap manis (søt soyasaus)</li>
		<li>1 ts flytende honning</li>
		<li>frisk koriander, finhakket</li>
	</ul>
	<p>Evt tilbehød: 3porsjoner ris eller nudler (billigste løsningen)</p>
	<p>Rør sammen ingrediensene til soyasausen. Smak deg frem her til du finner den balansen som du ønsker mellom de gode smakene. 
		Har du frisk ingefør kan dette også tilsettes litt av, revet.</p>
	<p>Krydre lakesen med salt og stek den ca 2-4min på hver side i stekepannen, til den er passe stekt. Bruk fjerne først en 
		nøytral olje og deretter en ss smør når laksen har fått stekeskorpe på begge sidene. Mot slutten av steketiden strør du 
		litt sesamfrø på laksen. Laksen er ferdigstekt, når den så vidt faller fra hverandre. La laksen hvile i noen minutter før 
		den serveres.</p>
	<p>Skjær opp grønnsakene som angitt ovenfor og wok dem raskt i en stekepanne, i en nøytral olje. Grønnsakene er ferdige når 
		de er nesten møre og fortsatt har litt ”spenst” igjen. Om man ikke får tak i pak choy, kan man evt heller benytte spinat.</p>
	<p>Det hele serveres samlet, med kokt ris eller nudler som tilbehør. Man har da til slutt soyasausen over den ferdige retten, 
		eller serverer den ved siden av slik at man kan forsyne seg etter smak og behag.</p>
