	<h4>Intervalltrening</h4>
	<p>Intervalltrening er en treningsform som har som mål å øke kondisjonen. Det bedrer spesielt hjertets evne til å pumpe blod til musklene. Denne formen for trening for trening består i at man veksler mellom hardt arbeid og hvile. Vi skriver da om to varianter, som er 45x15 intervall, og 4x4 intervall. Vi har tatt utgangspunkt i at man utfører intervallene på mølle.</p>

	<h4>Intervall 45x15</h4>
	<p>Oppvarming i 5 minutter, hvor man sakte øker hastigheten til man ender med ønsket start hastighet på den første arbeidsperioden.</p>
	<p>Etter oppvarmingen så starter man med en arbeidsperiode på 45 sekunder, med sin start hastighet, og hopper deretter av for hvileperioden på 15 sekunder. Så øker man hastigheten med 0.2 for hver hvileperiode, slik at om du hadde en start hastighet på 9.0, så økes den til 9.2, og slik blir det i hver hvileperiode.</p>
	<p>Intervallet er med 15 arbeidsperioder, og 15 hvileperioder, hvor den siste er på 2 minutter. Etter den siste hvileperioden, så skal man avslutte intervallet med 5 arbeidsperioder, og 4 hvileperioder uten at man øker hastigheten.</p>
	<p>Total tid ca for et slikt intervall er 27-30 minutter.</p>
