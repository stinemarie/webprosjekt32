	<h4 id="langloping">Langløping</h4>
	<p>Man starter økten med et rolig tempo og ikke sprint på med en gang, for da vil man ikke holde ut lenge. Så øker man tempoet en smule for ca hver kilometer, slik at den siste kilometeren blir hard, men ikke utmattende. Så kan man ta roligere økter, hvor man ikke øker tempoet for hver kilometer, men løper slik at man kan ha en føre en samtale mens du øker, men fortsatt kjenner at du må konsentrere deg om pusten. For hver økt man har med langløpring bør man øke intensiteten, slik at man får dekket en lengre distanse, og får en forbedring.</p>

	<h4 id="spinning">Spinning</h4>
	<p>Ved å dra på timer, så kan man variere mellom intervall og langkjøring her også. Intervall – da har man ”topper” hvor man har en høy intensitet, hvor man gir alt, også har man en pause mellom hver ”topp” som er på ca 1min. Ved langkjøring, så holder man en jevn intensitet, hvor det varierer mellom å ”stå” og ”sitte” på setet, og variasjon mellom bakke (høy motstand, sakte tråkk) og sletter (lavere motstand, raske tråkk). Det er også ofte variasjon i timene, hvor man veksler mellom å ha stående- og sittende posisjon.</p>
