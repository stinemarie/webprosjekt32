	<p>Oppvarming i 5-10 minutter, deretter 4 harde arbeidsperioder, hver på 4 minutter, 2 minutter hvileperiode mellom arbeidsperiodene. 
		Intensiteten skal være høy (opp mot 85-95% av makspuls), og tempoet skal helst være omtrent likt i alle arbeidsperiodene.</p>

