<h2>Kondisjonstrening</h2>

<h4 id="kondis">Intervall 15x15</h4>
<p>Oppvarming i 5min, deretter har man 15 arbeidsperioder, hvor man har 14 hvileperioder mellom hver arbeidsperiode. Arbeidsperiode er 
	på 45sekunder, mens man trår av mølla i hvileperiodene. Deretter har man 2min pause etter fullførte 15 arbeidsperioder. Hvor man etter 
	disse to minuttene avslutter med 5 arbeidsperioder, med 4 hvileperioder som igjen er mellom hver arbeidsperiode. Evt så kan man 
	ha 20 arbeidsperioder, med 19 hvileperioder, for så å ha 2min pause, og avslutter med 5 arbeidsperioder, med 4 hvileperioder. Etter 5min 
	oppvarming har man en start hastighet, evt 9. For hver fullførte arbeidsperiode, så øker man hastigheten med 0.2, slik at etter 
	første arbeidsperiode med hastighet 9.0, så starter man den andre arbeidsperioden med hastigheten 9.2</p>

<h4>Intervall 4x4</h4>
<p>Oppvarming i 5-10 minutter, deretter 4 harde arbeidsperioder, hver på 4 minutter, 2 minutter hvileperiode mellom arbeidsperiodene. 
	Intensiteten skal være høy (opp mot 85-95% av makspuls), og tempoet skal helst være omtrent likt i alle arbeidsperiodene.</p>

<h4 id="langloping">Langløping</h4>
<p>Løper/jogger med rolig tempo, og øker intensiteten for hver langløping. Viktig at man starter økten rolig for å klare å fullføre 
	totalt for eksempel en times økt. For hver økt, om man øker intensiteten, så vil man da få en lengre distanse for hver gang.</p>

<h4 id="spinning">Spinning</h4>
<p>Ved å dra på timer, så kan man variere mellom intervall og langkjøring her også. Intervall – da har man ”topper” hvor man har en høy 
	intensitet, hvor man gir alt, også har man en pause mellom hver ”topp” som er på ca 1min. Ved langkjøring, så holder man en jevn 
	intensitet, hvor det varierer mellom å ”stå” og ”sitte” på setet, og variasjon mellom bakke (høy motstand, sakte tråkk) og sletter 
	(lavere motstand, raske tråkk)</p>