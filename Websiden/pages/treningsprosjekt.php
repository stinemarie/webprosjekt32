<h2>Før/Etter med TTFS</h2>

<p>Vi har under prosjektperioden gått inn for å ha et Før og Etter prosjekt, som Ida og Christopher har utført. Her satte begge to seg noen
	små oppnåelige mål for den korte perioden vi hadde, og så skulle vi se hvilke av disse målene som vi følte at vi klarte oppfylle etter
	at prosjektperioden var over.</p>

<h4>Ida</h4>
<img src="" alt="Ida Før prosjektet">
<p>Jeg følte at for meg var det ikke å oppnå å gå ned så, så mange kilo på den ene måneden som vi skulle ha dette prosjektet. For meg så gikk
	det mer ut på å få gjennomført de øktene som jeg vet skal til for at jeg skal kunne sitte igjen med en god selvfølelse. 
	Mine mål ble derfor:</p>
	<ul>
		<li>Få trent minst 3 ganger hver uke</li>
		<li>I disse øktene skulle jeg ha minimum 2 intervaller og 2 styrkeøkter fordelt</li>
		<li>Unngå kollektiv</li>
	</ul>
<!--><img src="" alt="Før prosjektet"><!-->

<p>Nå som vi er ferdig med prosjektet, så kan jeg si at jeg har klart å følge alle mine mål, unntatt den ene uken, da jeg var syk, og kun
fikk gjort to av tre ønskede økter den uken.</p>
