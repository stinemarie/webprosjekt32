<h2>Hvor kan man trene?</h2>
	<p>Vi har valgt å gå ut fra de sentrene som man finner i Oslo, ettersom vi retter oss mot studenter i Oslo. Under vil du da se hvilke
		senter som man finner i Oslo, og hva de forskjellige sentrene tilbyr, hvor i Oslo de ligger og hvilke åpningstider de forskjellige 
		har.</p>

