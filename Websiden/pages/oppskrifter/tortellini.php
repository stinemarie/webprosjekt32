<h4>Tortellini med tomater og ruccula (2porsjoner+)</h4>
<ul>
	<li>1 pakke Tortellini med kjøtt- eller skinkefyll</li>
	<li>1 liten Rødløk, i biter</li>
	<li>1-2 fedd Hvitløk, finhakket</li>
	<li>1 rød Paprika, i biter</li>
	<li>1/3 Squash, i biter</li>
	<li>1 boks Hermetiske tomater</li>
	<li>En neve rucculasalat (eller evt babysalat)</li>
	<li>Finhakkede friske urter</li>
	<li>Salt og nykvernet pepper</li>
	<li>Parmesan</li>
</ul>
<p>Freser rødløken i litt olje på middels varme, til løken er blank og myk. Tilsett paprika, squash og hvitløk, og 
	la det surre med et par minutter. Tilsett hermetiske cherrytomater og la sausen stå og småputre ca 10 min. Smak 
	til med salt og pepper. Kok tortellini etter anvisningen på pakken.</p>
<p>Rett før servering tilsetter du tortellinien og lar den putre med til den er gjennomvarm. Tilsett ruccula og friske 
	krydderurter, og rør forsiktig sammen. Retten er ferdig når salaten såvidt har falt sammen.</p>
<p>Serveres med godt brød og høvlet parmesan til.</p>



