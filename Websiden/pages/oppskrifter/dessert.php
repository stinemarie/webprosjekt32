	<h4>Franks bringebærterte med vaniljekrem(4porsjoner)</h4>
	<ul>
		<li>2,5dl hvetemel</li>
		<li>1 ss sukker</li>
		<li>110 g smør</li>
		<li>4-8 ss kaldt vann</li>
		<li>1 kurv friske bringebær</li>
		<li>ca 4 dl vaniljekrem (<a href="#vaniljekrem">Her kan man lage egen</a> eller kjøpe ferdig)</li>
		<li>melisdryss</li>
	</ul>
			
	<p>Ta deigen på melet bakebord og del den i 4 like store emner (eller du kan selvsagt bake en stor terte om du heller ønsker det). Kjevle hvert av emnene til 10-12 cm i diameter. Legg tertebunnene på bakepapirkledt stekebrett.</p>
	<p>Lag vaniljekrem, etter feks denne oppskriften, eller bruk ferdigkjøpt som jeg gjorde. Tertene blir selvsagt aller best om du lager vaniljekremen selv, men av og til er det ikke tid til det og da får nest-best duge. Fordel 2-3 ss vaniljekrem på hver av tertebunnene, men la det være igjen en kant på ca 2 cm ytterst. Fordel bringebærene oppå kremen. Brett deigkanten godt innover slik at den såvidt dekker de ytterste bærene.</p>
	<p>Stek tertene midt i ovnen (over- og undervarme) ved ca 200 grader i ca 20-25 minutter, til tertebunnen er gyldenbrun. Jeg stekte mine terter i overkant av 25 minutter. Avkjøl tertene litt på rist. (Dersom du lager en stor terte må du beregne noe lengre steketid.)</p>
	 
