<h4>Hjemmelaget pizza</h4>
	<p>Pizzabunn:</p>
	<ul>
		<li>5,5 dl Mel/Pizzamel</li>
		<li>2,5 dl Vann (25-30grader)</li>
		<li>25 g fersk gjær</li>
		<li>Ca 1/2 ts Havsalt</li>
		<li>En klype sukker</li>
		<li>1 ss Olivenolje</li>
	</ul>
	<p>Ha mel, salt, sukker, gjær, olivenolje og mesteparten av vannet i bakebollen. Kjør blandingen i kjøkkenmaskin med eltekroker 
		på lav til middels hastighet i ca 10 minutter. Spe eventuelt med mere vann dersom deigen blir tørr.</p>
	<p>Dekk bakebollen med lokk eller plast og la deigen etterheve til ca dobbel størrelse. Det tar en times tid, men du kan gjerne 
		la den stå enda lengre om du har tid.</p>
	<p>Har du ikke kjøkkenmaskin rører du ut gjæren i litt av vannet og rører deretter de øvrige ingrediensene sammen. Elt deretter 
		så mye du synes du har tid og ork til, og sett til heving. </p>
	<p>Selve pizzaen:</p>
	<ul>
		<li>Pizzabunn</li>
		<li>Pizzasaus</li>
		<li>6-8 Skiver god Spekeskinke/Chorizo/Pepperoni</li>
		<li>1-2 Mozzarella, i skiver</li>
		<li>Nykvernet sort pepper</li>
		<li>Friske krydderurter, for eksempel oregano</li>
	</ul>
	<p>Legg pizzabunnen på et bakepapirkledt stekebrett. Fordel pizzasaus og garnityr over pizzaen etter smak og behag. </p>
	<p>Dra bakepapiret over på den glovarme pizzasteinen og la pizzaen steke på 250-300 grader (eller enda varmere, om mulig), til 
		pizzaen er ferdigstekt. Steketiden vil variere ut fra steketemperatur, ovnens egenskaper og hva du fyller pizzaen med. Jeg 
		stekte pizzaen i ca 7 minutter på 300 grader. Etter ca 3 minutter napper du eventuelt ut bakepapiret dersom det lett lar seg fjerne.</p>
	<p>Server gjerne pizzaen med et dryss med høvlet parmesan, litt chili-/basilikumolje, friske krydderurter, og gjerne et glass 
		italiensk rødvin til.</p>

