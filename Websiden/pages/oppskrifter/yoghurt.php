	<h4>Yoghurt med hjemmelaget granola</h4>
	<p>Hjemmelaget granola:</p>
	<ul>
		<li>250 g Havregryn (økologisk)</li>
		<li>50 g Solsikkekjerner</li>
		<li>50 g Sesamfrø</li>
		<li>50 g Mandler, grovt hakket</li>
		<li>50 g Valnøtter, grovt hakket</li>
		<li>25 g Brunt sukker</li>
		<li>2 klyper Maldonsalt</li>
		<li>1 dl Eplejuice (eller annen fruktjuice)</li>
		<li>1 ss Honning</li>
		<li>1 ss Smør</li>
		<li>Litt kanel (smak til), kan sløyfes</li>
		<li>75-100 g Tørket eple (om man ønsker), i biter</li>
	</ul>
	<p>Rør sammen alt det tørre med unntak av sukker, epler og kanel. Ha eplejuice, smør, sukker og honning i en kasserolle, og varm opp til alt er oppløst. Smak eventuelt til med kanel. Hell blandingen oppi det tørre og rør godt sammen.</p>
	<p>Hell blandingen over på et bakepapirkledt stekebrett og fordel den jevnt utover. Stek granolaen midt i ovnen ved 140 grader (på vanlig over- og undervarme) i ca 50 minutter, til blandingen har en jevn brunfarge. Rør i blandingen cirka hvert 10-15 minutt for å få et jevnt stekt resultat. Hvis du skal doble oppskriften må du steke det i to omganger.</p>
	<p>La blandingen avkjøles, tilsett tørkede eplebiter, og hell deretter blandingen på et stort glass eller i en boks. Granolaen er holdbar i ca 1 måned.</p>

