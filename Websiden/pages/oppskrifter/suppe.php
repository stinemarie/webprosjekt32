	<h4>Bønne- og linsesuppe med chorizo(3porsjoner+)</h4>
	<ul>
		<li>1 løk, finhakket</li>
		<li>½ - ¾ rød chili (eller mer/mindre etter ønske), finhakket</li>
		<li>1,5 terninger kyllingbujong/kyllingfond</li>
		<li>3 ss tomatpure</li>
		<li>1 boks hermetiske tomater, hakket</li>
		<li>6-7 dl vann</li>
		<li>1 boks hvite bønner</li>
		<li>3 dl linser</li>
		<li>5 soltørkede tomater, i biter</li>
		<li>5 vårløk, i skiver</li>
		<li>salt og nykvernet sort pepper</li>
		<li>ca 1 ts revet ingefær</li>
		<li>100 g chorizo, i terninger (dette kan man skippe om man vil)</li>
		<li>Finhakket koriander til pynt</li>
	</ul>
	<p>Fres løk, hvitløk og chili i litt olje (og salt evt for at det ikke skal brenne), til løken er myk og blank. 
		Tilsett tomatpuré, buljong/fond, hermetiske tomater, vann, bønner, linser og soltørkede tomater, og gi det hele et oppkok. 
		Smak til med ingefær. La det stå og putre under lokk i ca 10min. I mellomtiden så kan du steke vårløken og chorizoen i litt smør og sette det til side.</p>
	<p>Når suppen er kokt ferdig og har fått litt tykkelse, tilsetter man vårløk og chorizo, og smak deretter til suppen med 
		salt og pepper. Strø over litt finhakket koriander og server evt med en liten klatt creme fraiche og ferskt brød til.</p>

