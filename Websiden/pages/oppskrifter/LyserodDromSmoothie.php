<h4>Lyserød Drøm</h4>
	<ul>
		<li>1 Gulrot</li>
		<li>3-4 Cherrytomater</li>
		<li>1/2 Rød paprika</li>
		<li>1 Banan</li>
		<li>3-4 Valnøtter</li>
		<li>Litt caynnepepper</li>
		<li>2 dl Eplejuice</li>
		<li>6 Isbiter</li>
	</ul>
	<p>Skrell gulrot, rens paprika og skjær dem i biter. Ha eplejuicen i blenderen først. Deretter tar man i alle de andre ingrediensene
		så kjører man blandingen til man får en glatt konsistens.</p>

