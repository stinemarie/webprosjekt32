<h4>Ajopachi</h4>
<ul>
	<li>1 Rød Paprika</li>
	<li>1/2 Rød chili</li>
	<li>100 g friske jordbær</li>
	<li>199 g fryste jordbær</li>
	<li>2 Appelsiner</li>
	<li>2 dl Appelsinjuice</li>
</ul>
<p>Skjær skallet av appelsinene og del dem i biter. Rens paprika for stilk, frø og hinner, så deler man den i biter. Så renser man de friske 
	jordbærene. Ha appelsinjuicen i blenderen, deretter resten av ingrediensene. Kjør blandingen til du får en glatt konsistens.</p>

