<h4>Skippern</h4>
<ul>
	<li>1 Liten avokado</li>
	<li>1 dl Spinat</li>
	<li>1 ss Hasselnøtter</li>
	<li>1 ts Revet ingefær</li>
	<li>2 dl Eplejuice</li>
	<li>6 Isbiter</li>
</ul>
<p>Vask spinaten godt og fjern de tykkeste stilkene. Om spinaten din er av den grove sorten med tykke blader, hell kokende vann
	over, og skyll deretter med kaldt vann. La den renne godt av etterpå. Del avokadoen i to, fjern steinen og skjær i biter. Ha eplejuicen
	først i blenderen, deretter resten av ingrediensene. Kjør blandingen til du får en glatt konsistens.</p>

