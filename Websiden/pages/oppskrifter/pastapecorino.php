<h4>Pasta med spicy tomatsaus og pecorino</h4>
<ul>
	<li>1 Løk, finhakket</li>
	<li>2-3 ss Olivenolje</li>
	<li>100 g Pancetta eller Bacon, i strimler (alternativt)</li>
	<li>1/2-1 Rød chili, finhakket</li>
	<li>2 Bokser hermetiske tomater</li>
	<li>1 ss tomatpuré</li>
	<li>Nykvernet sort pepper</li>
	<li>Litt salt</li>
	<li>Pasta til ca 4 porsjoner</li>
	<li>Pecorino (eller parmesan), revet</li>
	<li>Frisk basilikum</li>
</ul>
<p>Fres løken i oljen, til løken er blank og myk. Tilsett pancetta/bacon, og la det surre med noen minutter. Dersom du 
	ønsker en vegatarisk rett kan du droppe kjøttet. Tilsett hermetiske tomater (av god kvalitet), tomatpurè og chili (smak til), 
	og la det putre uten lokk i ca 10 minutter. Smak til med salt og pepper.</p>
<p>Kok pastaen etter anvisningen på pakken, til den er al dente. Mer om koking av pasta kan du lese her. Bruk eventuelt fullkornspasta 
	hvis du ønsker et sunnere alternativ. Ha den ferdigkokte pastaen oppi tomatsausen og vend det hele forsiktig sammen. 
	Serveres umiddelbart med revet pecorino (eventuelt parmesan) og noen basilkumblader. Server gjerne godt brød som tilbehør.</p>


