<h4>Sitruskylling med asparges og timian (2porsjoner)</h4>
	<ul>
		<li>2 Kyllingfileter</li>
		<li>Salt og pepper</li>
		<li>2 fedd Hvitøk, finhakket</li>
		<li>1/2 Sitron, saft og skall</li>
		<li>1/2 Appelsin, saft og skall</li>
		<li>Frisk timian, hakket</li>
		<li>2 Klatter smør</li>
		<li>1 Sitron, delt i to</li>
		<li>8 Asparges</li>
		<li>Litt sukker og salt</li>
	</ul>
	<p>Krydre kyllingfiletene med salt og pepper og brun raskt på begge sider. Stek den delte sitronen sammen med kyllingen, med snittsiden 
		ned. Legg filetene i et lite ildfast fat og hell appelsin- og sitronsaften over, og fordel finhakket hvitløk, sitrusskall, smør og 
		hakket timian over. Legg sitronene oppi formen, med snittsiden opp. Stek kylling og sitronen ved ca 200 grader i ca 10-15 minutter, til 
		kyllingen er gjennomstekt. La kyllingen hvile i ca 5 minutter før servering. Ta vare på sitrussjyen og ha den i en liten sausemugge.</p>
	<p>Kok aspargesen 2-3 minutter i rikelig med vann tilsatt litt salt og sukker. Ha den ferdigkokte aspargesen i kaldt vann slik at du får 
		stanset kokeprosessen.</p>
	<p>Anrett aspargesen på en tallerken og legg kyllingfileten oppå. Drypp litt av sitrussjyen over og pynt med frisk timian. Serveres med resten 
		av sitrussjyen, brød, ris eller annet tilbehør.</p>

