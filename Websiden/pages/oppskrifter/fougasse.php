<h4>Fougasse fylt med kylling, bacon, salat, tomat, rød pesto og aioli.</h4>
<ul>
	<li>2 Fougasse</li>
	<li>2 Kyllingfileter</li>
	<li>2 Skiver bacon</li>
	<li>Ruccula</li>
	<li>Cherrytomater, delt i tre skiver</li>
	<li>Fetaost, i terninger</li>
	<li>Rød pesto</li>
	<li>Aioli</li>
	<li>Friske krydderurter</li>
	<li>Nykvernet sort pepper</li>
</ul>
<p>Gni kyllingen inn med salt og nykvernet pepper og stek den et par minutter på hver side, til den får en fin stekeskorpe. 
	Etterstek kyllingen ved ca 200 grader i ca 10-15 minutter, til den er gjennomstekt. La filetene hvile noen minutter, før du 
	skjærer filetene opp i skiver. Sprøstek baconskivene og la de renne av på litt kjøkkenpapir.</p>
<p>Dersom du ikke har fougasse, kan du for eksempel benytte foccacia, som er omtrent det samme (eller annet godt brød). Del brødene 
	i to på langs og smør rød pesto på den ene delen og aioli på den andre. Skyll salaten og la den renne av, og fordel den deretter 
	på den nederste delen. Ha deretter på kylling, sprøstekt bacon, fetaost, cherrytomater og krydderurter. Ha gjere litt nykvernet 
	sort pepper over og ringle eventuelt over litt god olivenolje (eller vent med det til den skal serveres, om du skal ha den med 
	deg på tur). Legg toppen oppå, skjær brødet i to, og server – eller pakk de inn i plast og ta de med på tur!</p>

