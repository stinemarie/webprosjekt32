	<h4>Potet- og spinatsalat med laks (2porsjoner)</h4>
	<p>Laks og salat:</p>
	<ul>
		<li>2 biter Fersk laks</li>
		<li>4-8 poteter, avhengig av størrelsen</li>
		<li>En neve spinat, skylt og renset</li>
		<li>3-4 Vårløk, snittet</li>
		<li>2-3 Reddiker, i tynne skiver</li>
		<li>Friske krydderurter, for eksempel persille og dill</li>
	</ul>
	<p>Dressing:</p>
	<ul>
		<li>2 ts Grov sennep</li>
		<li>1 fedd Hvitløk, finhakket</li>
		<li>3 ss Olivenolje</li>
		<li>2 ss Hvitløkseddik</li>
		<li>Litt revet sitronskall</li>
		<li>Salt og pepper</li>
	</ul>
	<p>Vask potetene godt og kok dem møre i lettsaltet vann. Bruk gjerne nypoteter hvis du har.</p>
	<p>Krydre laksen med salt og grovkvernet sort pepper. Stekes ved 200 grader (midt i ovnen ved over- og undervarme) 
		i 10-12 minutter, til laksen er passe stekt. Del laksen opp i passende biter.</p>
	<p>Ha ingrediensene til dressingen i et glass med lokk eller lignende. Rist det hele godt sammen. Smak til med salt og pepper.</p>
	<p>Del potetene i grove biter og ha dem i en salatbolle sammen med spinat, vårløk, reddiker og krydderurter. Hell over dressingen 
		og vend salaten forsiktig sammen.</p>

