<h4>Solskinn</h4>
<ul>
	<li>1/2 Gul paprika</li>
	<li>1 Gulrot</li>
	<li>1 Moden Mango</li>
	<li>2 dl Appelsinjuice</li>
	<li>6 Isbiter</li>
</ul>
<p>Del mangoen, fjern steinen og skallet. Skrell og rens paprikaen, så skjær den i biter. Ha appelsinjuicen i blenderen, 
	deretter paprika, gulrot, mango og isbiter. Kjør den til den er jevn og fin</p>

