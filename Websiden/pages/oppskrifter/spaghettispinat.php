<h4>Spaghetti med spinat, soltørket tomat, hvitløkschips og feta (4porsjoner)</h4>
<ul>
	<li>4 porsjoner Spaghetti (gjerne fullkorn)</li>
	<li>1/2 dl Olivenolje</li>
	<li>1 Hvitløk, i tynne skiver på langs</li>
	<li>1 Rødløk, i tynne båter</li>
	<li>10 Soltørkede tomater, i biter</li>
	<li>250 g spinat, renset og vasket</li>
	<li>1-1,5 dl av pastavannet</li>
	<li>Maldonsalt og nykvernet sort pepper</li>
	<li>100 g Fetaost</li>
</ul>
<p>Rens hvitløken og del hvert fedd i 2-3 skiver på langs. Ha olivenoljen i en liten panne eller kjele og la hvitløken 
	kose seg i oljen til den blir lett gylden og sprø, etter ca 4 minutter. Pass på at oljen ikke blir for varm for da blir 
	hvitløken raskt svidd. Ha hvitløken på et kjøkkenpapir slik at fettet renner av.</p>
<p>Kok spaghettien etter anvisningen på pakken. Ta vare på 1,5 dl av pastavannet og hell av resten.</p>
<p>Mens pastaen koker freser du rødløken på middels varme til den er blank og myk. Tilsett soltørket tomat og spinaten, og 
	rør det hele godt sammen. Spinaten er ferdig når den såvidt faller samme, etter et par minutter.</p>
<p>Tilsett pastaen, rør det hele forsiktig sammen og spe med pastavannet du har tatt vare på. Smak til med salt og pepper.</p>
<p>Smuldre fetaost over retten og server umiddelbart, gjerne med godt brød til.</p>



