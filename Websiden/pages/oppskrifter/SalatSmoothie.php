<h4>Salatsmoothie</h4>
<ul>
	<li>3 blader Romanosalat</li>
	<li>100 g Brokkoli, lett forvellet (dvs et lett oppkok)</li>
	<li>1/2 Avokado</li>
	<li>1/2 Agurk</li>
	<li>1/4 Honningmelon</li>
	<li>1 ts Revet ingefær</li>
	<li>2 dl Eplejuice</li>
	<li>6-8 Isbiter</li>
</ul>
<p>Skyll, tørk og riv salaten i biter. Fjern skall og steiner fra honningmelonen og skjær den i biter. Del avokadoen i to, fjern steinen
	og skjær i biter. Del agurken i biter, og gi brokkolien et lett oppkok. Hell eplejuicen i blenderen, og deretter ha resten av ingrediensene
	i blenderen. Kjørt alt til du får en lysegrønn farge.</p>

