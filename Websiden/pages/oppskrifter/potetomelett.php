	<h4>Potetomelett med brokkoli og salami (2porsjoner+)</h4>
	<ul>
		<li>2 Kokte poteter, i skiver</li>
		<li>1/4 Brokkoli, i små buketter</li>
		<li>1/2 Rødløk, i biter</li>
		<li>4 Egg</li>
		<li>4 ss Vann</li>
		<li>Salt og nykvernet pepper</li>
		<li>ca. 75 g Vellaget salami, strimlet</li>
		<li>ca. 50 g vallaget ost, i små terninger</li>
		<li>Frisk persille, finhakket</li>
	</ul>
	<p>Fres løk, poteter og brokkoli i smør eller olje, til løken blir blank og myk.</p>
	<p>Rør sammen egg, vann, salt og pepper. Hell eggeblandingen over grønnsaksblandingen. Fordel osteterninger og salami over. 
		La det stå et øyeblikk og begynn deretter å skrape fra siden inn mot midten med en flat sleiv (men ikke bruk metall). Stek 
		omeletten ved å skyve den stivnede massen inn mot midten, slik at det som fortsatt er flytende kommer i direkte kontakt med 
		pannen. Senk temperaturen, legg på lokk og la omeletten stå å trekke til eggemassen har stivnet.</p>
	<p>Gi potetomeletten et lite dryss av hakket persille og server. Omeletten kan serveres varm eller kald, gjerne sammen med en 
		god salat og godt brød som tilbehør. Eventuelle rester er godt egnet i lunsjboksen dagen etter.</p>

