<h4>Squashsalat med kylling, perlespelt og basilikumpesto (2porsjoner)</h4>
<ul>
	<li>1 dl Perlespelt, tilberedt etter anvisningen på pakken</li>
	<li>1 Squash</li>
	<li>2 Kyllingfileter</li>
	<li>Rucculasaltat</li>
	<li>Parmesan, høvlet</li>
	<li>Friske krydderurter, feks basilikum</li>
	<li>1-2 ss pinjekjerner, ristet</li>
	<li>Salt og pepper</li>
	<li>Litt god balsamicoeddik</li>
	<li>3-4 ss basilikumpesto/grønn pesto</li>
</ul>
<p>Skyll perlespelten i et dørslag eller lignende og kok den etter anvisningen på pakken. La gjerne perlespelten avkjøles noe før du 
	benytter den i salaten. Har du ikke perlespelt kan du for eksempel benytte bulgur, couscos eller ris.</p>
<p>Krydre kyllingen med salt og pepper og stek den raskt på begge sider i stekepannen til du får en fin stekeskorpe. La kyllingen 
	ettersteke i stekovnen ved ca 180 grader, til den er gjennomstekt. Det tar ca 10 minutter avhengig av størrelsen på kyllingfiletene. 
	La kyllingen hvile i minst 5 minutter, før du skjærer den opp i passende biter.</p>
<p>Del squashen i to og skjær hver bit i jevne skiver på langs. Pensle squashskivene med olivenolje og stek dem på middels varme på 
	begge sider i en grillpanne eller stekepanne, i ca 4 minutter – til squashen er passe mør. Krydre squashen med salt og pepper.</p>
<p>Ha rucculasalat, friske krydderurter og perlespelt i en salatbolle. Tilsett litt god olivenolje, balsamicoeddik, salt og pepper og vend 
	salaten forsiktig sammen. Anrett salaten i salatskåler og fordel squash, kyllingbiter, pesto og ristede pinjekjerner over. Skav over 
	litt parmesan og server, gjerne med konfiserte tomater og eltefritt speltloff til.</p>


