<h4>Dark blue heaven</h4>
	<ul>
		<li>100 g Slangeagurk</li>
		<li>125 g Friske blåbær</li>
		<li>100 g Frosne blåbær</li>
		<li>1 Banan</li>
		<li>2 dl Druejuice</li>
		<li>Saften av 1/2 lime</li>
	</ul>
	<p>Skrell bananen og skjør den i biter. Skjær agurken i biter. Ha juicen først i blendere, deretter blåbær, banan og slangeagurken.
		Kjør blandingen til du får en glatt konsistens. Spe på med mer væske for å få den tynnere, eller mer banan/bær for å få den tykkere om
		man ønsker det.</p>
