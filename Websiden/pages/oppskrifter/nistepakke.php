<h4>Nistepakke</h4>
	<ul>
		<li>Grovbrød</li>
		<li>Kremost/Philadelphia</li>
		<li>Ruccula</li>
		<li>Skinke av eget ønske</li>
		<li>Cherrytomater, i skiver</li>
	</ul>
	<p>Man kutter grovbrødet i ønsket tykkelse for å lage to stykk gode sandwicher. Så smører man på med et lag kremost, deretter legger
		man på ruccula salaten, skinken og cherrytomatene. Når alt innholdet er fint plassert, så legger man den ekstra skiven med grovbrød
		oppå. Deretter kytter man hele sandwichen på skrå, og pakker det enten inn i matpakkepapir (old school) eller så legger man det
		i matboksen, sammen med for eksempel litt druer og appelsinskiver.</p>

