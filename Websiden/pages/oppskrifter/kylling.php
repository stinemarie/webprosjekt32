<h4>Pasta med rød pesto, kylling og paprika (3porsjoner)</h4>
	<ul>
		<li>2 Kyllingfilleter</li>
		<li>Salt og pepper</li>
		<li>Smør/olje til steking</li>
		<li>1 Rødløk, i tynne skiver</li>
		<li>1 Porsjon rød pesto</li>
		<li>Parmesan, for litt luksus på toppen</li>
		<li>3 Porsjoner fullkornspasta</li>
		<li>Evt frisk persille eller andre krydderurter finhakket</li>
	</ul>
	<p>Kok pastaen som anvist på pakken. Tips til når den er ferdigkokt: Ha den i et dørslag og rør evt inn litt olje 
		(gjerne chiliolje for ekstra smak) slik at man hindrer at den kleber seg sammen.</p>
	<p>Krydre kyllingen med salt og pepper, og stek den raskt i smør/olje, til den er gjennomstekt. 
		Legg kyllingen til side, og fres rødløk og paprika til rødløken er blank og myk.</p>	
	<p>Bland sammen pasta, rød pesto, kulling og grønnsaker, og bland det hele forsiktig sammen. 
		Strø gjerne over de friske krydderurtene hvis du har, og server med noen flak parmesan. For en siste finish, 
		så kan du ha ferskt brød på siden!</p>
