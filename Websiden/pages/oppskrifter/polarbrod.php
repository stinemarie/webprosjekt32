<h4>Polarbrød (24 brød)</h4>
	<ul>
		<li>150 g Havregryn, finmalt</li>
		<li>500 g Fullkornshvetemel (eller annet finmalt grovt mel)</li>
		<li>ca. 300 g Siktet hvetemel</li>
		<li>ca. 15 g Fersk gjær eller 1/2 pose tørrgjær</li>
		<li>1 ts Sukker</li>
		<li>ca. 1 ts Salt</li>
		<li>4 1/2 dl Melk</li>
		<li>2 dl Vann</li>
		<li>1 dl Solsikkeolje eller annen nøytral matolje</li>
	</ul>
	<p>Kjør havregrynene i en foodprosessor til de er ganske finmalt. Ha alt det tørre i bakebollen sammen med gjæren. 
		Tilsett melk, vann (ca. 25 grader) og solsikkeolje, og kjør det på lav hastighet i kjøkkenmaskinen til alt er godt 
		blandet. Kjør det deretter på middels hastighet, totalt i ca. 10 minutter. Deigen skal nå være smidig og ikke for kompakt. 
		Sett deigen til heving på et lunt sted i ca. 1 time.</p>
	<p>Ha deigen på bakebordet og del den i to like store emner. Del hvert av emnene i ca. 12 jevnstore biter og trill til boller. 
		Kjevle bollene ut til de er ca 1/2 cm – og 10-11 cm i diameter. Legg brødene på bakepapirkledd stekebrett. Prikk brødene med 
		spisepinner eller lignende, og la dem etterheve i 10-15 minutter. Stekes midt i ovnen ved 225 grader (over- og undervarme) i 
		9-10 minutter, eller til brødene er gyldne under og ellers er gjennomstekte. Avkjøles på rist og under et klede, slik at de 
		holder seg myke.</p>
	<p>Når brødene er helt avkjølt bør de pakkes i plast og legges i fryseren. Ta opp ønsket antall brød og la dem tine i romtemperatur 
		(tiner på ca 30 minutter) eller i kjøleskapet (over natten, hvis du skal ha dem tidlig på morgenen). Ønsker du lunkne polarbrød 
		kan du varme dem ved ca. 175 grader varmluft, i 3-4 minutter.</p>

