<h4>Gulrot- og ananas smoothie (1porsjon)</h4>
	<ul>
		<li>2 skiver frisk ananas</li>
		<li>1 dl appelsinjuice</li>
		<li>1 stk gulrot</li>
		<li>2 dl yoghurt naturell</li>
	</ul>
	<p>Rens og skjær ananas og gulrot I biter. Ha appelsinjuicen I mikseren først – deretter yoghurt, ananas og gulot. 
		Kjør blandingen til en du har fått en glatt smotthie</p>
