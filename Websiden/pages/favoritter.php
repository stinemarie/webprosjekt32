<p> 
	Min favoritt er granateple. Den er proppet med antioksidanter. I følge 
	<a href="http://www.bama.no"> bama.no</a>  inneholder den 10 ganger mer 
	antioksidanter enn appelsiner og nesten 40 ganger mer enn epler. Både 
	håndgranater og byen Granada i Spania er oppkalt etter frukten. Den passer bra til å ha i 
	salat. Da gjerne med frisk salat, avokado, feta ost, rødløk og cherrytomater 
	nam nam.<br> PS! super digg å ha i daiquiri også! 
</p>

