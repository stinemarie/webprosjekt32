	<div class="favorittBoks">
		<h4>Ida sin favoritt: Avokado</h4>
		<figure>
			<img src="./img/avokado.jpg" alt="bilde av avokado">
			<figcaption>Foto: Anton Croos</figcaption>
		</figure>
		<p>Den er rik på kalsium, proteiner og B-, E-, K-, A- og C-vitaminer.  I tillegg inneholder frukten kolesterolsenkende stoffer og 
		antioksidanter som er spesielt bra for nervesystemet.</p>
		<p>Avokado vokser på trær og er en frukt, men siden den verken er søt eller inneholder fruktsyre blir den ofte oppfattet som en grønnsak. Smaken er mild, nøtteaktig og behagelig. Den høstes umoden, slik at den skal tåle transport og modnes underveis.</p>
		<p>Varianter av avokado:</p>
		<ul>
			<li>Hass</li>
			<li>Ettinger</li>
			<li>Fuerte</li>
			<li>Bacon</li>
		</ul>
		<p>Jeg personlig elsker å mose/skjære opp avokadoen å ha den på et grovt knekkebrød, så strø litt havsalt og svart knernet pepper over, og til slutt strø over ruccula, nam nam! Hvis ikke er det jo super godt å lage en god salat, eller en guacamole med litt for mye chili.</p>
		<p class="kilde"> Kilde: <a href="http://www.frukt.no/leksikon/gronnsaker/avokado/" target="_blank"> Frukt.no</a>, 
		<a href="http://www.kriweb.no/2013/02/14/kjottkaker-med-avokado/" target="_blank"> kriweb.no</a> og 
		<a href="https://itunes.apple.com/gb/app/z-food-nutrition-facts-lite/id484101503?mt=8" target="_blank"> Apple app: A-Z Food Nutrition Facts lite</a> </p>
