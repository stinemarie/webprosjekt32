<h3>Pilates</h3>

<p>Pilates, oppkalt etter sin skaper Josef Pilates, har blitt populært bandt annet for sin effektivitet. Treningsformen fokuserer på å styrke rygg og mage muskler. Øvelsene og rolige og skånsomme for kroppen. Her strekker man musklene så vel som å trene de. <br> Pilates kan være rolig og gi deg harmoni som hjelper deg gjennom en stressende hverdag.</p>

