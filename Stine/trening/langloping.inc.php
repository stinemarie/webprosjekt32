<h2>Langløping</h2>
<p>
	Løper/jogger med rolig tempo, og øker intensiteten for hver langløping
	Viktig at man starter økten rolig for å klare å fullføre totalt for eksempel en times økt.
	For hver økt, om man øker intensiteten, så vil man da få en lengre distanse for hver gang.
</p>

