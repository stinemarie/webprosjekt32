<h2>Spinning</h2>
<p>
	Ved å dra på timer, så kan man variere mellom intervall og langkjøring her også
	Intervall – da har man ”topper” hvor man har en høy intensitet, hvor man gir alt, også har man en pause mellom hver ”topp” som er på ca 1min.
	Ved langkjøring, så holder man en jevn intensitet, hvor det varierer mellom å ”stå” og ”sitte” på setet, og variasjon mellom bakke (høy motstand, sakte tråkk) og sletter (lavere motstand, raske tråkk)
</p>

