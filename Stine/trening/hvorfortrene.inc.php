<h1>Hvorfor trene?</h1>
<p>Det er utrolig viktig å sette av tid til trening hver uke. 
Trening er noe som gir deg mer energi til det du skal få gjort i hverdagen, 
samtidig så vil jevnlig trening gjøre at du styrker hjerte. Dette forebygger 
hjerte-karsykdommer.Det er viktig å kombinere med både styrke og kondisjon 
for å få litt variert trening. Husk også å få noen hviledager for å la kroppen 
få restituert.</p>

