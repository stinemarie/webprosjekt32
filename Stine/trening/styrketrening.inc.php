<h2>Styrketrening</h2>
<h3>Squats med vekter</h3>
<p>
	Fin øvelse for bein og rumpe.
	Man står med beina noe spredt, og går ned i knebøy. Her er det viktig at man har rett rygg.
</p>

<h3>Utfall</h3>
<p>
	En annen bra øvelse for bein og rumpe.
	Her føler jeg at man også får trent noe balanse om man velger gående utfall.
</p>

<h3>Push Up</h3>
<p>
	Denne er bra for amrer og rygg, hvor man kan bruke kun sin egen kroppsvekt, eller om man 
	ønsker en tyngre øvelse, så kan man legge vekt på ryggen.
<p>
<h3>Planke med ball</h3>
</p>
	Man starter i første posisjon, som på første bilde, og ruller ballen sakte frem, 
	slik at man ender i en planke stilling.
