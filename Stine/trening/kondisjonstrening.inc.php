<h2>Kondisjonstrening:<h2>

<h3>15/15 intervall</h3>
<p>
	generelt om kondisjonstrening......
</p>
<p>	
	Oppvarming i 5min, 
	deretter har man 15 arbeidsperioder, hvor man har 14 hvileperioder mellom hver arbeidsperiode. 
	Arbeidsperiode er på 45sekunder, mens man trår av mølla i hvileperiodene. 
	Deretter har man 2min pause etter fullførte 15 arbeidsperioder. 
	Hvor man etter disse to minuttene avslutter med 5 arbeidsperioder, 
	med 4 hvileperioder som igjen er mellom hver arbeidsperiode.
	Evt så kan man ha 20 arbeidsperioder, med 19 hvileperioder, 
	for så å ha 2min pause, og avslutter med 5 arbeidsperioder, med 4 hvileperioder.
	Etter 5min oppvarming har man en start hastighet, evt 9
	For hver fullførte arbeidsperiode, så øker man hastigheten med 0.2, 
	slik at etter første arbeidsperiode med hastighet 9.0, så starter 
	man den andre arbeidsperioden med hastigheten 9.2
</p>
<h3>4x4 intervall</h3>
<p>
	Oppvarming i 5-10 minutter, deretter 4 harde arbeidsperioder, hver på 4 minutter, 
	2 minutter hvileperiode mellom arbeidsperiodene. Intensiteten skal være høy 
	(opp mot 85-95% av makspuls), og tempoet skal helst være omtrent likt i alle 
	arbeidsperiodene.
</p>

